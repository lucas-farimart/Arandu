print("============================================")
print("      PROVA DA INVERSAO DE COLUNAS 8x8      ")
print("============================================\n")

print("-> Entrada normal:\n")

x1 = np.array([1, 2, 3, 4, 5, 6, 7, 8])
x1 = x1.T

w1 = np.array([
    [4, 2, 1, 3, 5, 7, 8, 6],
    [8, 1, 2, 6, 4, 3, 5, 7],
    [0, 2, 5, 4, 8, 1, 6, 3],
    [3, 8, 9, 1, 2, 4, 7, 5],
    [1, 4, 6, 2, 7, 5, 3, 8],
    [5, 3, 8, 7, 1, 6, 2, 4],
    [2, 7, 4, 5, 6, 8, 1, 3],
    [6, 5, 3, 8, 4, 2, 7, 1]
])

y1 = w1 @ x1

print("x1 =", x1, "\n")
print("w1 =\n", w1, "\n")
print("y1 = w1 @ x1 =",   y1, "\n")

print("\n-> Entrada invertida e colunas invertidas:\n")

x2 = x1[::-1]      # inverte o vetor
w2 = w1[:, ::-1]   # inverte as colunas
y2 = w2 @ x2

print("x2 =", x2, "\n")
print("w2 =\n", w2, "\n")
print("y2 = w2 @ x2 =",   y2, "\n")

if np.array_equal(y1, y2):
    print("\nMESMO RESULTADO !!\n")
else:
    print("\nRESULTADO DIFERE ...\n")

